#Positive Zahl
x = 12/7
print("x:", x)

rx = round(x,3)
print("x gerundet auf drei Stellen:", rx)
rx = round(x)
print("x gerundet auf null Stellen:", rx)
ix = int(x)
print("Das Ergebnis in eine ganze Zahl umgewandelt: ", ix)

#Negative Zahl
x = -12/7
print("x:", x)

rx = round(x,3)
print("x gerundet auf drei Stellen: ", rx)
rx = round(x)
print("x gerundet auf null Stellen: ", rx)
ix = int(x)
print("Das Ergebnis in eine ganze Zahl umgewandelt: ", ix)
